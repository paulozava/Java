
public class Variables {

	public static void main(String[] args) {
		int integerOne = 1;
		int integerTwo = 2;
		double doubleOne = 1.0;
		double doubleTwo = 2;
		
		System.out.println(integerOne / integerTwo);
		System.out.println(doubleOne / doubleTwo);
		System.out.println(integerOne / doubleOne);

	}

}
